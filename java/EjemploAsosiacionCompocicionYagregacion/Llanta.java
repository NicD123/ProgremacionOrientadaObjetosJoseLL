public class Llanta{
	private double altura;
	private double anchura;
	private double rin;
	public Llanta( double alto, double ancho, double radio){
		altura = alto;
		anchura = ancho;
		rin = radio;
		System.out.println("Construyendo una llanta altura = " + altura + ", anchura = " + anchura + ", radio =" + rin);
	}
}